/**
 * בחירת וריאציה דטרמיניסטית לפי אינדקס - שלא יתחלף בכל רנדר.
 */
function pickVariant<T>(arr: T[], seed: number): T {
  return arr[seed % arr.length];
}

/**
 * מחזיר משפט מתאים של התוכי לפי השאלה הנוכחית באי.
 * - מותאם לגיל הילד (כיתות א-ב לעומת ג+)
 * - בוחר וריאציה דטרמיניסטית לפי אינדקס
 */
export function getParrotSpeechForQuestion(
  topic: Topic,
  grade: Grade,
  questionIndexInIsland: number,
  totalQuestionsInIsland: number
): string {
  const parrot = PARROTS[topic];
  const tier = grade <= 2 ? parrot.younger : parrot.older;
  const remaining = totalQuestionsInIsland - questionIndexInIsland;

  if (questionIndexInIsland === 0) {
    return pickVariant(tier.intro, 0);
  }
  if (questionIndexInIsland === totalQuestionsInIsland - 1) {
    return pickVariant(tier.lastQuestion, questionIndexInIsland);
  }
  // אמצע - לסירוגין: לפעמים waitingSpeech עם N, ולפעמים midSpeech
  if (questionIndexInIsland % 2 === 1) {
    return pickVariant(tier.waitingSpeech, questionIndexInIsland).replace(
      "{n}",
      remaining.toString()
    );
  }
  return pickVariant(tier.midSpeech, questionIndexInIsland);
}

/** מחזיר את משפט החופש של התוכי - לפי גיל. */
export function getParrotFreeSpeech(topic: Topic, grade: Grade): string {
  const parrot = PARROTS[topic];
  const tier = grade <= 2 ? parrot.younger : parrot.older;
  return pickVariant(tier.free, Math.floor(Math.random() * tier.free.length));
}
