function pick(arr: string[]): string {
  return arr[Math.floor(Math.random() * arr.length)];
}

export function praiseForCorrect(): string {
  return pick(PRAISE_FOR_CORRECT);
}

export function praiseForTrying(): string {
  return pick(PRAISE_FOR_TRYING);
}

export function praiseForSkip(): string {
  return pick(PRAISE_FOR_SKIP);
}
